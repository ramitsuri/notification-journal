#!/bin/bash
############################################################
# Run                                                      #
############################################################
Run()
{
  if ! ./gradlew clean assembleDebug -PdisablePreDex --stacktrace;
  then
    exit 1
  fi

  if ! ./gradlew core:jvmTest;
  then
    exit 1
  fi
}

############################################################
############################################################
# Main program                                             #
############################################################
############################################################
Run

